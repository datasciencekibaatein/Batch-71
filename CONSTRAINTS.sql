-- CONSTRAINTS ARE RULES APPLIED TO TABLE COLUMNS TO MAINTAIN DATA INTEGRITY,ACCUARCY, AND RELIABILITY IN DATABASE.

-- NOT NULL (IT ENSURES THAT YOU ARE NOT INSERTING THE NULL VALUES)
-- UNIQUE (YOU ARE INSERTING THE UNIQUE VAULES, IT WILL ALLOW NULL SINGLE TIME)
-- PRIMARY KEY (COMBINATION , NOT NULL + UNIQUE)
-- FOREIGN KEY 
-- CHECK (VALIDATES THE VALUE)
-- DEFAULT (IT SETS DEFAULT)
-- AUTO INCREMENT (AUTOMATICALLY GENERATE THE NUMBER)


-- NOT NULL CONSTRAINTS
CREATE TABLE STUDENTS(
	STUDENT_ID INT NOT NULL,
    NAME VARCHAR(30),
    AGE int
    );
DROP TABLE STUDENTS;
INSERT INTO STUDENTS(NAME,AGE) VALUES("ROHAN",20);


-- UNIQUE CONSTRAINT

CREATE TABLE STUDENTS(
	STUDENT_ID INT,
    USER_NAME VARCHAR(30) UNIQUE,
    AGE int
);

