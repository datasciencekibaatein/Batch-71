-- COMPOSITE PRIMARY KEY
CREATE TABLE STUDENTS(
	STUDENT_ID INT,
    COURSE_ID INT,
    PRIMARY KEY(STUDENT_ID,COURSE_ID) -- COMPOSITE PRIAMRY KEY
);

SELECT * FROM STUDENTS;

-- CHECK CONSTRAINT
CREATE TABLE STUDENTS(
	STUDENT_ID INT,
    AGE INT,
    CHECK(AGE<=30) -- IT'LL NOT ALLOW TO INSERT THE AGE >30
);

